package com.arraygen.ngs.readqc;

public class PercentNucleotideBase {

	private double Apercent;
	private double Tpercent;
	private double Gpercent;
	private double Cpercent;
	private double Npercent;

	public Double getApercent() {
		return Apercent;
	}
	public void setApercent(Double apercent) {
		Apercent = apercent;
	}
	public Double getTpercent() {
		return Tpercent;
	}
	public void setTpercent(Double tpercent) {
		Tpercent = tpercent;
	}
	public Double getGpercent() {
		return Gpercent;
	}
	public void setGpercent(Double gpercent) {
		Gpercent = gpercent;
	}
	public Double getCpercent() {
		return Cpercent;
	}
	public void setCpercent(Double cpercent) {
		Cpercent = cpercent;
	}
	public Double getNpercent() {
		return Npercent;
	}
	public void setNpercent(Double npercent) {
		Npercent = npercent;
	}

}
